package Comparable_And_Comparator;

public class Laptop {
    private String brand;
    private int price;
    private int ram;

    public int getPrice(){
        return price;
    }
    public int getRam(){
        return ram;
    }

    public String getBrand(){
        return brand;
    }
    
    public void setRam(int ram){
        this.ram = ram;
    }

    public void setPrice(int price){
        this.price = price;
    }

    public void setBrand(String brand){
        this.brand = brand;
    }

    


}
