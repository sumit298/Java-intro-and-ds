public class PrettyPrinting {
    public static void main(String[] args) {
        float f = 482.8484f;
        System.out.printf("Formatted Float num is %.2f", f); //formats and rounding as well;
        // Printing the formatted Pi
        System.out.println();
        System.out.printf("Formatted Pi %.3f", Math.PI);
        System.out.println();
    }
}
